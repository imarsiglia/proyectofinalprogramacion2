    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        ArrayList<Persona> jugadores = encuentro.getLocal().getPersona();
        if(jugadores.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe registrar al menos un jugador para agregar goles");
            return;
        }
        
        String[] jugs = new String[jugadores.size()];
        int index = 1;
        
        for(Persona persona : jugadores){
            jugs[index - 1] = index + ". " +persona.toString();
            index++;
        }
        
        String jugador = (String)
                JOptionPane.showInputDialog(this, 
                        "Seleccione un jugador", 
                        "Nuevo Gol", 
                        JOptionPane.INFORMATION_MESSAGE, 
                        null, jugs, jugs[0]);
        
        int selected = Integer.parseInt(jugador.substring(0, 1));
        Gol gol = new Gol(CampeonatoController.incremente++, encuentro, jugadores.get(selected-1));
        this.encuentro.getGolesLocal().add(gol);
        this.modelLocal.addElement(jugador.substring(2));
        
        this.updateGoles();
        
    }//GEN-LAST:event_jButton2ActionPerformed


    private void postInit() {
        
        this.setLocationRelativeTo(null);
        this.modelLocal = new DefaultListModel<>();
        this.modelVisitante = new DefaultListModel<>();
        this.jList1.setModel(this.modelLocal);
        this.jList2.setModel(this.modelVisitante);
        
        this.jLabel2.setText(this.encuentro.getLocal().getNombre());
        this.jLabel3.setText(this.encuentro.getVisitante().getNombre());
        this.encuentro.setJugado(true);
        
        for(Gol gol : this.encuentro.getGolesLocal()){
            this.modelLocal.addElement((gol.getPersona().getNombre() + " " + gol.getPersona().getApellido()).toUpperCase());
        }
        
        for(Gol gol : this.encuentro.getGolesVisitante()){
            this.modelVisitante.addElement((gol.getPersona().getNombre() + " " + gol.getPersona().getApellido()).toUpperCase());
        }
        
        this.updateGoles();
        
    }
    
    private void updateGoles(){
        this.jLabel4.setText("Goles: " + this.encuentro.getGolesLocal().size());
        this.jLabel5.setText("Goles: " + this.encuentro.getGolesVisitante().size());
    }

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String nombre = jTextField1.getText();
        DefaultTableModel model2 = getInitModel();
        this.jTable2.setModel(model2);

        if (!CampeonatoController.getInstance().getSelected().getEquipos().isEmpty()) {

            int index = 1;

            for (CampeonatoEquipo campeonatoEquipo : 
                    CampeonatoController.getInstance().getSelected().getEquipos()) {

                Equipo equipo = campeonatoEquipo.getEquipo();
                if (equipo.getNombre().toUpperCase().contains(nombre.toUpperCase())) {

                    String[] row = {
                        String.valueOf(index),
                        equipo.getNombre(),
                        String.valueOf(equipo.getPersona().size()),
                        "0"
                    };

                    model2.addRow(row);
                    index++;
                }
            }
        }
        jTable2.updateUI();

    }//GEN-LAST:event_jButton4ActionPerformed


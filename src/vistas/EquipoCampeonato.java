    public void buildData() {
        
        int size = CampeonatoController.getInstance().getSelected().getEquipos().size();
        DefaultTableModel model2 = new DefaultTableModel(new String[]{
            "No.", "Nombre", "Jugadores", "Campeonatos"}, 0);
        this.jTable2.setModel(model2);

        if (size > 0) {

            int index = 1;

            for (CampeonatoEquipo equipoCameponato : CampeonatoController.getInstance().getSelected().getEquipos()) {
                
                Equipo equipo = equipoCameponato.getEquipo();
                
                String[] row = {
                    String.valueOf(index),
                    equipo.getNombre(),
                    String.valueOf(equipo.getPersona().size()),
                    "0"
                };

                model2.addRow(row);
                index++;

            }
        }

        jTable2.updateUI();
    }


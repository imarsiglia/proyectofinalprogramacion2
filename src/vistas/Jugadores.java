    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        
        String nombre = JOptionPane.showInputDialog("Ingrese nombre de jugador");
        String nombre1="", apellido="";
        for(int k=0;k<nombre.length();k++){
            if(nombre.charAt(k)==' '){
                nombre1=nombre.substring(0,k);
                apellido=nombre.substring(k+1,nombre.length());
            }
        }
        
        Equipo equipo = new Equipo();
        equipo.getNombre();
        if (nombre1 != null) {    
            PersonaController.getInstance().addPersonas(
                    new Persona(++PersonaController.incremente,equipo ,nombre1, apellido));
        }
        this.buildData();
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
    

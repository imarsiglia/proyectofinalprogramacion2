    private void buildData(){
        
        int size = PersonaController.getInstance().getPersonas().size();
        DefaultTableModel model = new DefaultTableModel(new String[]{
        "No.","Nombre", "Apellido"}, 0);
        this.jTable12.setModel(model);
        
        if (size > 0) {
            
            int index = 1;
            
            for (Persona persona : PersonaController.getInstance().getPersonas()) {
                
                String[] row = {
                    String.valueOf(index),
                    persona.getNombre(),
                    persona.getApellido()
                
                };
                    
               
                
                model.addRow(row);
                index++;
                
            }
        }
        
        jTable12.updateUI();
    }


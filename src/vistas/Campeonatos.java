    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String nombre = jTextField1.getText();
        DefaultTableModel model = getInitModel();
        this.tabla3.setModel(model);
        
        if (!CampeonatoController.getInstance().getCampeonatos().isEmpty()) {
            
            int index = 1;
            
            for (Campeonato campeonato : CampeonatoController.getInstance().getCampeonatos()) {
                
                if (campeonato.getNombre().toUpperCase().contains(nombre.toUpperCase())) {
                    
                    String[] row = {
                        String.valueOf(index),
                        campeonato.getNombre(),
                        String.valueOf(campeonato.getEquipos().size()),
                        "0"
                    };
                    
                    model.addRow(row);
                    index++;
                }
            }
        }
        tabla3.updateUI();

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
        int[] rows = (this.tabla3.getSelectedRows());
        if (rows.length == 1) {
            CampeonatoController.getInstance().setSelected(CampeonatoController.getInstance().getCampeonatos().get(rows[0]));
            this.setVisible(false);
            new GestionCampeonato().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un equipo para ver jugadores");
        }
        
          
    }//GEN-LAST:event_jButton2ActionPerformed
    


    private void buildData() {
        int size = CampeonatoController.getInstance().getCampeonatos().size();
        DefaultTableModel model = new DefaultTableModel(new String[]{
            "No.", "Nombre", "Equipos"}, 0);
        this.tabla3.setModel(model);
        
        if (size > 0) {
            
            int index = 1;
            
            for (Campeonato campeonato : CampeonatoController.getInstance().getCampeonatos()) {
                
                String[] row = {
                    String.valueOf(index),
                    campeonato.getNombre(),
                    String.valueOf(campeonato.getEquipos().size()),
                    "0"
                };
                
                model.addRow(row);
                index++;
                
            }
        }
        
        tabla3.updateUI();
    }


    private void postInit() {
       
        this.setLocationRelativeTo(null);
        
        this.model = (DefaultTableModel) jTable1.getModel();
        this.fechas = CampeonatoController.getInstance().getSelected().getFecha();
        
        for(Fecha fecha : this.fechas){
            for(Encuentro encuentro : fecha.getEncuentros()){
                
                Object[] row = new Object[5];
                row[0] = encuentro.getLocal().getNombre();
                row[1] = encuentro.getGolesLocal().size();
                row[2] = "VS";
                row[3] = encuentro.getGolesVisitante().size();
                row[4] = encuentro.getVisitante().getNombre();
                
                this.model.addRow(row);
                
            }
        }
                
    }
